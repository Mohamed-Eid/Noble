    public function make_defualt(City $city)
    {
        $cities = City::all();
        foreach ($cities as $_city) {
            $_city->is_default = 0;
            $_city->save();
        }
        $city->is_default = 1;
        $city->value = 1;
        $city->save();

        return redirect()->route('dashboard.currencies.index');
    }

